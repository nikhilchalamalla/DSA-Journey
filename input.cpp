#include<bits/stdc++.h>
using namespace std;

int main() {

    //Single Input and output
    int a;
    cout << "Enter the value: ";
    cin >> a ;
    cout << "Value is: " << a ; 


    //Taking Double Inputs and Outputs:
    int x, y;
    cout << "Enter the value of X: and Y: " ;
    cin >> x >> y;
    cout << "Value of x is: " << x << " and Y is: " << y ;
    return 0;
}